CREATE TABLE ProfessionalStaff (
    StaffID varchar(255) PRIMARY KEY NOT NULL,
    FirstName varchar(255),
    MiddleName varchar(255),
    LastName varchar(255),
    Email varchar(255),
    Status varchar(255),
    HighDegree varchar(255),
    YearAwarded varchar(255),
    College varchar(255),
    HireTerm varchar(255),
    Rank varchar(255)
);